BORDER_THIN = Border(
    left=Side(style='thin', color='FFBFBFBF'),
    right=Side(style='thin', color='FFBFBFBF'),
    top=Side(style='thin', color='FFBFBFBF'),
    bottom=Side(style='thin', color='FFBFBFBF'),
)

def style_header(ws, row, start_col, end_col, text_color='FFFFFFFF', fill=C_HEADER):
    for col in range(start_col, end_col + 1):
        c = ws.cell(row=row, column=col)
        c.font = _font(size=11, bold=True, color=text_color)
        c.fill = _fill(fill)
        c.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
        c.border = BORDER_THIN

def style_sub_header(ws, row, start_col, end_col):
    for col in range(start_col, end_col + 1):
        c = ws.cell(row=row, column=col)
        c.font = _font(size=10, bold=True)
        c.fill = _fill(C_SUB)
        c.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
        c.border = BORDER_THIN

def autosize(ws, widths):
    for i, w in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w


