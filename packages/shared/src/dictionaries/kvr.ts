// ────────────────────────────────────────────────────────────
// 1. Интерфейс
// ────────────────────────────────────────────────────────────

export interface KvrEntry {
  /** Код КВР (3-значный, строка для сохранения ведущих нулей) */
  code: string;
  /** Полное наименование вида расходов */
  name: string;
  /** Группа (первая цифра) */
  group: '1' | '2' | '3' | '4' | '5' | '6' | '7' | '8' | '9';
  /** Подгруппа (первые две цифры) */
  subgroup: string;
  /** Применимость к закупкам (true = используется в procrement-строках) */
  usedInProcurement: boolean;
}

